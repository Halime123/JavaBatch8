package com.syntax.class08;

public class Repl51 {

	public static void main(String[] args) {
		int a=10;
		do {
			System.out.print(a);
			a++;
		} while(a<20);
		System.out.println("--------------");
		int b=0;
		do {
			System.out.print("Hello World!");
			b++;
		} while(b<5);
		System.out.println("--------------");
		int c=20;
		do {
			System.out.println(c);
			c-=2;
		} while(c>1);

	}

}
