package com.syntax.class08;

public class ForLoop {

	public static void main(String[] args) {
		for(int i=1; i<5; i++) {
			System.out.println("Good morning");
			
		}
		for(int a=1; a>=-10;a--) {
			System.out.print(a+" ,");
			
			
		}
		System.out.println("-----------");
		for (int b=10;b>0;b--) {
			System.out.print(b+", ");
		}
		System.out.println("--------------");
		for (int c=0; c<=30; c+=3) {
			System.out.print(c+" ");
		}
		System.out.println("----------------");
		for(int d=10; d>0; d-=2) {
			System.out.println(d+"  ");
		}
		
	}

}
