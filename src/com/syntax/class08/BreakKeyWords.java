package com.syntax.class08;

public class BreakKeyWords {

	public static void main(String[] args) {
		//break
		for(int i=1; i<4; i++) {
			System.out.println("hello");
			break;
		}
		//print numbers from to 1 to10
		for (int i=1; i<=10; i++) {
			if(i==5) {
				break;
			}
			System.out.print(i);
			
		}
		
	}

}
