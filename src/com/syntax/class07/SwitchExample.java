package com.syntax.class07;

public class SwitchExample {

	public static void main(String[] args) {
		String month="July";
		switch(month) {
		case"January":
		case"February":
		case "March":
			System.out.println("It is winter");
			break;
		case "April":
		case "May":
		case "July":
			System.out.println("spring");
			break;
			
				
			
		}

	}

}
