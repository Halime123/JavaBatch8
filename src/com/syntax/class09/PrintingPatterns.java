package com.syntax.class09;

public class PrintingPatterns {

	public static void main(String[] args) {
		
		for(int i=1;i<6;i++) {
			for (int j=1; j<6;j++) {
			System.out.print(j);
			
		}
			System.out.println();
	}

		for(int i=7;i>=1;i--) {
			for (int j=7; j>=1;j--) {
			System.out.print(j);
			
		}
			System.out.println();

}
}
}